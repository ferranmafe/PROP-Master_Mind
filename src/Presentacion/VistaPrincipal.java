    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            ArrayList<String> info = CP.cargarPartida();
            if (info.get(1).equals("CODEMAKER")) {
                VistaCodeMaker vm = new VistaCodeMaker(CP,this,info.get(3),info.get(4));
                vm.cargarPartida(info);
                vm.setVisible(true);
            }
            else {
                VistaCodeBreaker vb = new VistaCodeBreaker(CP,this);
                vb.cargarPartida(info);
                vb.setVisible(true);
            }
            this.dispose();
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed


